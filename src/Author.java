public class Author {
    String author_id;
    String first_name;
    String second_name;
    public Author(String aid, String fname, String sname){
        author_id = aid;
        first_name = fname;
        second_name = sname;
    }
    public Author(){
        author_id = "";
        first_name = "";
        second_name = "";
    }

}

